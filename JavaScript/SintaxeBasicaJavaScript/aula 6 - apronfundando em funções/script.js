//// tipos de função

// declarativa
function funcao(){
    console.log('Sou uma mensagem de uma funçaõ declarativa')
}

funcao();

/// expressão de função
// com nomeação obs, é opcional

var funcao1 = function funcao1(){
    console.log('Sou uma mensagem de função de expressão')
}

funcao1();

//arrow function
var funcao2 = () => {
    console.log('Sou uma arrow function')
}

funcao2();
